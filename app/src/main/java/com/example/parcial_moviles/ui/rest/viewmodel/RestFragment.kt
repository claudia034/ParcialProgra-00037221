package com.example.parcial_moviles.ui.rest.viewmodel

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.fragment.app.activityViewModels
import com.example.parcial_moviles.databinding.FragmentViewRestBinding

class RestFragment: Fragment(){
    private val restaurantViewModel: RestaurantViewModel by activityViewModels {
        RestaurantViewModel.Factory
    }

    private lateinit var binding : FragmentViewRestBinding

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

    }

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        binding = FragmentViewRestBinding.inflate(inflater, container, false)
        return binding.root
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        binding.viewmodel = restaurantViewModel
    }
}